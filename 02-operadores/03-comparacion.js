let a = 10;

console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);


console.log(a == 10); // el doble = pregunta si los valores son iguales, pero NO si los tipos son iguales
console.log(a != 10); // el ! evalua si son diferentes
console.log(a == "10"); // asi evalua si es igual al string

// vamos a usar siempre los simbolos de abajo, los de arriba se usaban anteriormente.

console.log(a === "10"); // el triple = pregunta o evalua si el valor o string en este caso es igual Y ADEMAS el tipo de dato.
console.log(a !== "10"); // con el ! evaluamos sin son el mismo valor y tipo de dato